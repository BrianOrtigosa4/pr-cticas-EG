<ol>
        <li value = 1> Qué es HTML, cuando fue creado, cuáles fueron las distintas versiones y cuál es la última?</li>
        <li>¿Cuáles son los principios básicos que el W3C recomienda seguir para la creación de documentos
con HTML?</li>
        <li>En las Especificaciones de HTML, ¿cuándo un elemento o atributo se considera desaprobado? ¿y
obsoleto?</li>
        <li>Qué es el DTD y cuáles son los posibles DTDs contemplados en la especificación de HTML 4.01?</li>
        <li>Qué son los metadatos y cómo se especifican en HTML?</li>
</ol>

<ol>
    <li velue = 1>HTML (HyperText Markup Language) es el lenguaje estándar para estructurar páginas web. Creado en 1991 por Tim Berners-Lee.
Versiones: HTML 2.0 (1995), 3.2 (1997), 4.01 (1999), XHTML (2000), HTML5 (2014).
Última: HTML5.3 / HTML Living Standard (WHATWG).</li>
    <li><ul>
        <li>Separar contenido y presentación.</li>
        <li>Usar etiquetas semánticas correctas.</li>
        <li>Accesibilidad para todos los usuarios.</li>
        <li>Compatibilidad hacia adelante y hacia atrás.</li>
    </ul></li>
    <li><ul>
        <li>Desaprobado (deprecated): aún funciona, pero se recomienda no usarlo.</li>
        <li>Obsoleto (obsolete): ya no debe usarse y puede no funcionar en navegadores.</li>
    </ul></li>
    <li>DTD (Document Type Definition): define la estructura y reglas de un documento HTML.
En HTML 4.01 hay tres:
    <ul>
        <li>Strict (sin etiquetas obsoletas).</li>
        <li>Transitional (permite elementos de presentación).</li>
        <li>Frameset (permite uso de frames).</li>
    </ul></li>
    <li>Metadatos: información sobre el documento (autor, descripción, codificación, etc.).
Se especifican en la etiqueta <head> con <meta>.</li>
</ol>
